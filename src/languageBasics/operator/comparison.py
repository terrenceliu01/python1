"""
Comparison Operators
== Equal
!= NotEqual
>  Greater than
<  Less than
>= Greater than or equal to
<= Less than or equal to
"""
a, b = 10, 20
f = a==b
# comparison operator always return True or False bool type.
print(type(f))
print(f)

print(a == b)
print(a != b)
print(a <= b)
print(a >= b)
print(a > b)
print(a < b)
print()

